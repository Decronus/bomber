function openPopupCons() {
    var popupCons = document.getElementById('popup-cons-back');
    var popupContainerCons = document.getElementById('popup-container-cons');

    popupCons.style.opacity = "100%";
    popupCons.style.visibility = "visible";
    
    popupContainerCons.style.opacity = "100%";
    popupContainerCons.style.transform = "translate(0px, 0)";
}

function closePopupCons() {
    var popupCons = document.getElementById('popup-cons-back');
    var popupContainerCons = document.getElementById('popup-container-cons');

    popupCons.style.opacity = "0";
    popupCons.style.visibility = "hidden";

    popupContainerCons.style.opacity = "0";
    popupContainerCons.style.transform = "translate(0px, -100%)";
}

